/**
 * 取得商品的完整搜尋字串（名稱 + 分類 + 品牌 + 標籤）
 */
export function buildProductSearchString(
  product: { id: string; name: string; category: string; brand: string },
): string {
  const tags = PRODUCT_SEARCH_TAGS[product.id] ?? [];
  return [product.name, product.category, product.brand, ...tags].join(" ").toLowerCase();
}
